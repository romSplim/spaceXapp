import UIKit

extension UIColor {
    class var myColor: UIColor {
        return UIColor(red: 0.96, green: 0.96, blue: 0.96, alpha: 1)
    }
    
    class var myColor2: UIColor {
        return UIColor(red: 0.79, green: 0.79, blue: 0.79, alpha: 1)
    }
    
    class var myLightGray: UIColor {
        return UIColor(red: 0.56, green: 0.56, blue: 0.56, alpha: 1)
    }
    
    class var myBackgroundGray: UIColor {
        return UIColor(red: 0.13, green: 0.13, blue: 0.13, alpha: 1)
    }
    
}
